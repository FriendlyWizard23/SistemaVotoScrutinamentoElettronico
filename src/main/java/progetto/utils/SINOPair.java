package progetto.utils;

public class SINOPair {
	private int numero_si;
	private int numero_no;

	public SINOPair(int numero_si, int numero_no) {
		super();
		this.numero_si = numero_si;
		this.numero_no = numero_no;
	}

	public int getNumero_si() {
		return numero_si;
	}

	public void setNumero_si(int numero_si) {
		this.numero_si = numero_si;
	}

	public int getNumero_no() {
		return numero_no;
	}

	public void setNumero_no(int numero_no) {
		this.numero_no = numero_no;
	}

}
